class Person
  
  def initialize(name, job)
    @name
    @job
  end
  
  # -----------------------
  # Getters
  
  def name
    @name
  end
  
  def job
    @job
  end
  
  -----------------------
  # Setters
  
  def name=(new_name)
    @name=new_name
  end
  
  def job=(new_job)
    @job=new_job
  end
  
end